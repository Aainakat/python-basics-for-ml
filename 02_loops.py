# For loop example
print("For Loop Example:")
for i in range(1, 6):
    print(i)

print("\nWhile Loop Example:")
# While loop example
count = 1
while count <= 5:
    print(count)
    count += 1

# Loop with list
skills = ["Python", "AI/ML", "Design"]
print("\nMy Skills:")
for skill in skills:
    print(skill)
